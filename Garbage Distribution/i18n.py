def t(key: str, lang: str, **kwargs) -> str:
    """Return translated text for the key in the given language."""
    lang = lang if lang in TRANSLATIONS else DEFAULT_LANGUAGE
    template = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key) or key
    if kwargs:
        try:
            return template.format(**kwargs)
        except Exception:
            return template
    return template


